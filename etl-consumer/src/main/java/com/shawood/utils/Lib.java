package com.shawood.utils;

public class Lib {
    public String lib_version;
    public String lib;
    public String app_version;
    public String lib_method;
    public String lib_detail;

    public String getLib_version() {
        return lib_version;
    }

    public void setLib_version(String lib_version) {
        this.lib_version = lib_version;
    }

    public String getLib() {
        return lib;
    }

    public void setLib(String lib) {
        this.lib = lib;
    }

    public String getApp_version() {
        return app_version;
    }

    public void setApp_version(String app_version) {
        this.app_version = app_version;
    }

    public String getLib_method() {
        return lib_method;
    }

    public void setLib_method(String lib_method) {
        this.lib_method = lib_method;
    }

    public String getLib_detail() {
        return lib_detail;
    }

    public void setLib_detail(String lib_detail) {
        this.lib_detail = lib_detail;
    }
}
